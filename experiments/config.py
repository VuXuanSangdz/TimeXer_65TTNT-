from __future__ import annotations

from dataclasses import asdict, dataclass
from pathlib import Path

import torch
from torch.utils.data import DataLoader

from src.data.loaders import ExogenousForecastDataset, load_csv_dataset, split_by_time
from models.baselines import BASELINE_MODELS
from models.TimeXer import TimeXer


@dataclass(frozen=True)
class TrainConfig:
    seq_len: int = 96
    pred_len: int = 24
    batch_size: int = 64
    train_ratio: float = 0.7
    val_ratio: float = 0.1
    test_ratio: float = 0.2
    lr: float = 1e-3
    optimizer: str = "Adam"
    loss: str = "MSE"
    epochs: int = 12
    patience: int = 4
    patch_len: int = 8
    d_model: int = 128
    n_heads: int = 4
    e_layers: int = 2
    dropout: float = 0.1
    scaler: str = "StandardScaler (fit on train only)"
    default_seeds: tuple[int, ...] = (42, 123, 456)


TRAIN = TrainConfig()


@dataclass
class DatasetConfig:
    name: str
    csv_path: Path
    endo_col: str
    exo_cols: list[str]
    data_source: str
    collection_note: str


