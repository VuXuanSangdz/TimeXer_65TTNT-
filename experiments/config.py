def get_device() -> torch.device:
    return torch.device("cuda" if torch.cuda.is_available() else "cpu")


def experiment_manifest() -> dict:
    return {
        "train_config": asdict(TRAIN),
        "datasets": {
            k: {
                "name": v.name,
                "csv_path": str(v.csv_path),
                "endo_col": v.endo_col,
                "exo_cols": v.exo_cols,
                "data_source": v.data_source,
                "collection_note": v.collection_note,
            }
            for k, v in DATASETS.items()
        },
        "implementation_note": (
            "Standalone simplified reimplementation inspired by thuml/TimeXer. "
            "Not a line-by-line fork of the official run.py pipeline."
        ),
    }


def build_loaders(cfg: DatasetConfig, seq_len=None, pred_len=None, batch_size=None):
    seq_len = seq_len or TRAIN.seq_len
    pred_len = pred_len or TRAIN.pred_len
    batch_size = batch_size or TRAIN.batch_size

    endo, exo, _ = load_csv_dataset(cfg.csv_path, cfg.endo_col, cfg.exo_cols)
    (tr_e, tr_x), (va_e, va_x), (te_e, te_x) = split_by_time(
        endo, exo, train_ratio=TRAIN.train_ratio, val_ratio=TRAIN.val_ratio
    )

    train_ds = ExogenousForecastDataset(tr_e, tr_x, seq_len, pred_len, fit_scaler=True)
    val_ds = ExogenousForecastDataset(
        va_e, va_x, seq_len, pred_len, train_ds.scaler_endo, train_ds.scaler_exo
    )
    test_ds = ExogenousForecastDataset(
        te_e, te_x, seq_len, pred_len, train_ds.scaler_endo, train_ds.scaler_exo
    )

    loaders = {
        "train": DataLoader(train_ds, batch_size=batch_size, shuffle=True),
        "val": DataLoader(val_ds, batch_size=batch_size, shuffle=False),
        "test": DataLoader(test_ds, batch_size=batch_size, shuffle=False),
    }
    return loaders, train_ds, cfg


def build_model(name: str, n_exo: int, seq_len=None, pred_len=None):
    seq_len = seq_len or TRAIN.seq_len
    pred_len = pred_len or TRAIN.pred_len
    cls = BASELINE_MODELS[name]
    common = dict(seq_len=seq_len, pred_len=pred_len, n_exo=n_exo)
    if name == "TimeXer":
        return cls(**common, patch_len=TRAIN.patch_len, d_model=TRAIN.d_model, e_layers=TRAIN.e_layers)
    if name == "PatchTST":
        return cls(**common, patch_len=TRAIN.patch_len, d_model=TRAIN.d_model, e_layers=TRAIN.e_layers)
    if name == "iTransformer":
        return cls(**common, d_model=TRAIN.d_model, e_layers=TRAIN.e_layers)
    return cls(**common)
